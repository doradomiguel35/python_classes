class boolean_compare:
	def bool_less(self,num1,num2):
		if num1 < num2:
			return True 
		else:
			return False

	def bool_greater(self,num1,num2):
		if num1 > num2:
			return True
		else:
			return False
		
	def bool_equal(self,num1,num2):
		if num1 == num2:
			return True
		else:
			return False

