class float_operations:
	def float_add(a,b):
		result = float(a)+float(b)
		return result

	def float_sub(a,b):
		result = float(a) - float(b)
		return result

	def float_divide(a,b):
		result = float(a) / float(b)
		return result

	def float_mult(a,b):
		result = float(a) * float(b)
		return result

	def float_floor(a,b):
		result = float(a) // float(b)
		return result

	def float_exponent(a,b):
		result = float(a) ** float(b)
		return result
		
	def float_modulo(a,b):
		result = float(a) % float(b)
		return result
