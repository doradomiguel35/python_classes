class identify_type:
	def string_type_int(self,int):
		return str(type(int))

	def string_type_float(self,float):
		return str(type(float))

	def string_type_str(self,string):
		return str(type(string))

	def string_type_bool(self,bool):
		return str(type(bool))

	def string_type_list(self,list):
		return str(type(list))

	def string_type_set(self,set):
		return str(type(set))

	def string_type_tuple(self,tuple):
		return str(type(tuple))
