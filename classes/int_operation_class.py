class int_operation:
	def int_add(self,a,b):
		result = a+b
		return result
		
	def int_sub(self,a,b):
		result = a-b
		return result

	def int_multiply(self,a,b):
		result = a*b
		return result

	def int_divide(self,a,b):
		result = a/b
		return result

	def int_floor(self,a,b):
		result = a//b
		return result

	def int_exponent(self,a,b):
		result = a**b
		return result

	def int_modulo(self,a,b):
		result = a%b
		return result
